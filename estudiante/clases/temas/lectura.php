	<div class="texto"><h1>¿Qué es la Lectura?</h1> <br>

<p align=justify>La lectura es una actividad que consiste en interpretar y descifrar, mediante la vista, el valor fónico de una serie de signos escritos, ya sea mentalmente (en silencio) o en voz alta (oral). Esta actividad está caracterizada por la traducción de símbolos o letras en palabras y frases dotadas de significado, una vez descifrado el símbolo se pasa a reproducirlo. Es hacer posible la interpretación y comprensión de los materiales escritos, evaluarlos y usarlos para nuestras necesidades.</p>
<br>
<p align=justify>Como proceso mental, la lectura es una traducción de símbolos a ideas en la que se aprehende determinada información. Es la primera toma de contacto con el tema y un paso fundamental para el estudio. Requiere atención y concentración.</p>


