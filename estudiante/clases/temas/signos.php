<div class="contenido">
    <div class="titulo"><h1>Signos de Puntuación:</h1></div>
    <div class="texto"><h1>4. El punto y coma (;):</h1>
 <br>

<p align=justify>El punto y coma (;) representa una pausa mayor que la coma, pero menor que la del punto y seguido. Se emplea en los siguientes casos:</p>
<br>
<p align=justify>Para separar los elementos de una enumeración cuando se trata de expresiones complejas que incluyen comas. Por ejemplo, “Su pelo es castaño; los ojos, verdes; la nariz, respingona”.</p>
<br>
<p align=justify>Ante las conjugaciones (pero, aunque y más), cuando se introduce una frase larga. Por ejemplo, "Hace muchos años deseaba visitar ese lugar; pero hasta el sol de hoy no había tenido oportunidad".
</p>


<br>

