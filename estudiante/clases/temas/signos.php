<div class="contenido">
    <div class="titulo"><h1>Signos de Puntuación:</h1></div>
    <div class="texto"><h1>1. El punto (.):</h1>
 <br>

<p align=justify>indica la pausa que se produce al final de un enunciado. Después de punto siempre se escribirá con mayúscula, salvo en el caso de que aparezca en una abreviatura. Existen tres clases de punto:</p>
<br>
<p align=justify>El punto y seguido: se emplea para separar los distintos enunciados que forman un párrafo. Después de un punto y seguido se continúa a escribir en la misma línea.</p>
<br>
<p align=justify>El punto y aparte: separa párrafos distintos. Tras el punto y aparte, la escritura debe de continuar en la línea siguiente, en mayúscula y sangría.</p>
<br>
<p align=justify>El punto y final: es el punto que cierra un texto.</p>


<br>

</div>
    <div class="imagen">
        <img src="../../../img/punto.png">
    </div>
</div>

<div class="contenido">
    <div class="titulo"><h1>Signos de Puntuación:</h1></div>
    <div class="texto"><h1>2. La coma(,)</h1>
 <br>

<p align=justify>Marca una pausa breve dentro de un enunciado. Se emplea para separar componentes de la oración o sintagma, salvo que este precedido por alguna conjugación como y, e, o, u, ni. Por ejemplo, “Andrea llegó de la escuela, hizo los deberes, tomó baño y se durmió”.</p>
    <br>
<p align=justify>Se usa para encerrar incisos o aclaraciones y para señalar omisiones. Por ejemplo, “Si vienes, te esperamos; si no, nos vamos”.
Separa la parte entera de un número de la parte decimal. Por ejemplo, 3,5 km. Las locuciones conjuntivas o adverbiales van precedidas y seguidas de coma. Por ejemplo, en efecto, es decir, en fin.
</p>


<br>

</div>
    <div class="imagen">
        <img src="../../../img/coma.png">
    </div>
</div>



<div class="contenido">
    <div class="titulo"><h1>Signos de Puntuación:</h1></div>
    <div class="texto"><h1>3. Los dos puntos (:):</h1>
 <br>

<p align=justify>Los dos puntos (:) representan una pausa mayor que la coma, pero menor que la del punto. Se usa en los siguientes casos:</p>
<br>
<p align=justify>Antes de una cita textual y como un llamado de atención. Por ejemplo, “Dice el refrán: más vale tarde que nunca”.
Antes de una enumeración. Por ejemplo, “Las cuatro estaciones del año son: primavera, verano, otoño e invierno”.
Tras las fórmulas de cortesía que encabezan las cartas y documentos. Por ejemplo, "Estimada profesora:"
</p>


<br>

