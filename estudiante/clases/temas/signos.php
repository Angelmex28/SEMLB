<div class="contenido">
    <div class="titulo"><h1>Signos de Puntuación:</h1></div>
    <div class="texto"><h1>5. Signos de interrogación(¿?) y de admiración(¡!):</h1>
 <br>

<p align=justify>El uso de los signos de interrogación (¿?) marca el principio y el fin de una pregunta formulada de manera directa. Por ejemplo, “¿Qué quieres?”.</p>
<br>
<p align=justify>Los signos de admiración o exclamación (¡!) son usados en los enunciados que expresan un sentimiento o emoción intensa. Por ejemplo, “¡qué fracaso!”, “¡fuera de aquí!”. También, en las interjecciones, “¡ay!”, “¡oh!”.</p>
<br> 
<p align=justify>Cabe destacar que el uso de los signos de interrogación y admiración dobles, es decir, abiertos y cerrados, es exclusivo de la lengua castellana.</p>
<br>
<p align=justify>El uso de los signos de admiración e interrogación dobles se estableció por decisión de la Real Academia de la Lengua en el año 1754. Fue consecuencia de las continuas confusiones de lectura derivadas de la ausencia de elementos gráficos que anunciasen las preguntas o las admiraciones.</p>
<br>

</div>
