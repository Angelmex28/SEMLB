	<div class="texto"><h1>¿Qué son los Signos de Puntuación?</h1> <br>

<p align=justify>Los signos de puntuación son señales o marcas gráficas que permiten al redactor estructurar un discurso escrito, al tiempo que le permite al lector identificar las inflexiones del texto, es decir, el modo de entonación y las pausas necesarias que facilitan su comprensión.</p>
<br>
<p align=justify>Los signos de puntuación cumplen una importante función en la lengua escrita, pues su correcto uso permite comprender de forma coherente y sin ambigüedades el contenido de un texto.</p>
<br>
<p align=justify>Por medio de los signos de puntuación, se estructuran los textos, ordenando y jerarquizando las ideas en principales y secundarias, lo que permite al lector una mejor interpretación, análisis y comprensión del contenido. Dichos signos son los 
siguientes:</p>

<br>

