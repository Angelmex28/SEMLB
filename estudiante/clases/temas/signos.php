<div class="contenido">
    <div class="titulo"><h1>Signos de Puntuación:</h1></div>
    <div class="texto"><h1>6. Los puntos suspensivos (…):</h1>
 <br>

<p align=justify>Los puntos suspensivos (…) están formados por tres puntos en línea y sin espacio entre ellos. Se emplea en los siguientes casos:</p>
<br>
<p align=justify>Al final de las enumeraciones abiertas, con el mismo valor que el etcétera. Por ejemplo, “1, 2, 3, …”.</p>
<br>
<p align=justify>Cuando se deja una expresión incompleta o en suspenso. Por ejemplo, “A pocas palabras…”.</p>
<br>
<p align=justify>Para expresar dudas, temor o vacilación.</p>
<br>
<p align=justify>Cuando se reproduce de forma incompleta una cita textual, texto o refrán. Por ejemplo, "Cuando Gregorio Samsa se despertó (...), se encontró sobre su cama convertido en un monstruoso insecto" (Kafka, La metamorfosis).</p>
<br>

