    <div class="titulo"><h1>Propiedades de la División</h1></div>
    <div class="texto"><h1>Propiedades:</h1> <br>

<p align=justify>Una vez hayas aprendido qué es la división y las partes de la división, es importante conocer las propiedades de la división, es decir, las normas que determinan cómo deben realizarse cada uno de los tipos de división (enteras y exactas).</p>
<br>
<p align=justify>A continuación, te explicamos cuáles son las propiedades de la división:</p>
<br>
<p align=justify>1.Propiedad no conmutativa: en el caso de las divisiones, no se da la propiedad conmutativa. Al contrario de lo que sucede en la propiedad conmutativa de la multiplicación, si variamos el orden de los números de una división, se alterará el resultado. Por ejemplo: 10 ÷ 2 = 5,    pero    2 ÷ 10 = 0, 2.</p>
<br>
<p align=justify>2.Propiedad no asociativa: si descomponemos uno o todos los números de una división, o los agrupamos de diferentes maneras, el cociente o el resultado pueden cambiar. Por ejemplo: 200 ÷ 10 ÷ 5 puede dar 4 o 100 según como se asocie. Si realizamos (200 ÷ 10) ÷ 5 = 20 ÷ 5 = 4, pero es diferente a 200 ÷ (10 ÷ 5) = 200 ÷ 2 = 100.</p>
<br>
<p align=justify>3.Propiedad distributiva: es válida la propiedad distributiva de la división cuando se descompone el dividendo. Por ejemplo: 400 ÷ 10 = 200 ÷ 10 + 200 ÷ 10</p>
<br>
<p align=justify>Cero dividido entre cualquier número da cero: por ejemplo, "0 : 7 = 7".
No podemos dividir por cero, ya que no existe ningún cociente que multiplicado por 0 sea igual al dividendo.</p>
<br>
<p align=justify>4.Propiedad de la división exacta: llamamos división exacta a la división en la que el dividendo es igual al divisor por el cociente y el resto es cero. Por ejemplo: 10 ÷ 2 = 2 x 5</p>
<br>
<p align=justify>5.Propiedad de la división inexacta o entera: llamamos división inexacta o entera a la división en la que el dividendo es igual al divisor por el cociente más el resto. Es decir: D= d x c + r. Por ejemplo: 30 ÷ 7 = 4 (resto 2); por lo tanto, divisor x cociente + resto = 7 x 4 + 2 = 28 + 2 = 30 (dividendo).</p>



