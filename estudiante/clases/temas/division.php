	<div class="titulo"><h1>La División</h1></div>
	<div class="texto"><h1>¿Qué es la División?</h1> <br>

<p align=justify> En la matemática, la división es una operación parcialmente definida en el conjunto de los números naturales y los números enteros; en cambio, en el caso de los números racionales, reales y complejos es siempre posible efectuar la división, exigiendo que el divisor sea distinto de cero, sea cual fuera la naturaleza de los números por dividir. En el caso de que sea posible efectuar la división, esta consiste en indagar cuántas veces un número (divisor) está "contenido" en otro número (dividendo). 

<br>
<p align=justify>El resultado de una división recibe el nombre de cociente. De manera general puede decirse que la división es la operación inversa de la multiplicación, siempre y cuando se realice en un campo.</p>

<br>
<p align=justify>Es una de las cuatro operaciones básicas de la aritmética que consiste en averiguar cuántas veces un número (divisor) está contenido en otro número (dividendo). La división se puede considerar una operación equivalente a la resta ya que el número dividido se puede poner como equivalente en una resta, por ejemplo: 6 / 2 = 3 que corresponde a 6 – 2 = 4 (1ra resta), 4 – 2 = 2 (2da resta) y 2 – 2 = 0 (3ra resta), por lo tanto, se concluye que tenemos 3 restas y es el equivalente a dividir 6 / 2 = 3. División, del latín división, es el accionar y el resultado de dividir.</p>

