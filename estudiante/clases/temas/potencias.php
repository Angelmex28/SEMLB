	<div class="texto"><h1>¿Qué es una potencia?</h1> <br>

<p align=justify>La potencia o potenciación es una operación matemática que expresa de forma abreviada una multiplicación formada por varios números iguales ; es decir, es una simplificación de multiplicaciones donde se repite el mismo número:</p>
<br>
<p>4 ^ 3 = 4 x 4 x 4 = 64</p>
<br>
<p align=justify>Cualquier número entero elevado a la unidad (por ejemplo 3 1 siempre dará como resultado el mismo número, en este caso, 3):</p>
<br>
<p>3 ^ 1 = 3</p>
<br>
<p align=justify>Cualquier número entero elevado a cero (por ejemplo 5 0 siempre dará como resultado la unidad, es decir, 1 ):</p>
<br>
<p>5 ^ 0 = 1</p>
<br>
