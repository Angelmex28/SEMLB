    <div class="texto"><h1>Ortografía, educación y tecnología:</h1>
 <br>

<p align=justify>Vivimos en una era en la que ya no es necesario escribir a mano, y esto nos aleja considerablemente del lenguaje; por si fuera poco, todos los dispositivos que utilizamos para procesar texto están preparados para asistirnos, sea corrigiendo nuestros errores, o bien evitando que lleguemos a cometerlos, gracias a su función conocida como «autocompletar». No se puede justificar la decadencia que está sufriendo la ortografía con los avances tecnológicos, así como no se puede culpar al cine de la delincuencia juvenil.</p>
<br>
<p align=justify>En ambos casos, el problema reside en la educación, que es la base sobre la cual los seres vivos nos apoyamos para tomar decisiones. Si no nos enseñan a tiempo la importancia de una correcta ortografía, la gran diferencia que existe entre un texto rico y bien escrito y una sucesión casi aleatoria de pseudo términos sin signos de puntuación, entonces la tecnología representará nuestra única posibilidad de mantener vivo un legado que nos ha acompañado durante siglos.</p>

