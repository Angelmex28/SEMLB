    <div class="texto"><h1>Importancia de la ortografía:</h1>
 <br>

<p align=justify>La ortografía juega un rol vital en la estandarización de la grafía de una lengua, es decir, en la normalización, unificación y universalización de sus modos de escritura, de modo de garantizar el entendimiento de un escrito por cualquier hablante de la misma: en la medida en que las normas sean estables, los lectores podrán reconocer los sonidos inscritos en los signos visuales sin mayor problema. </p>
<br>
<p align=justify>Esto se hace muy evidente si pensamos que en el español del siglo XVIII existían altísimos márgenes de ambigüedad respecto de la grafía de letras cuyos sonidos son similares, como b/v, c/s/z, y/ll o g/j, así como de utilización de signos de puntuación y de acentos ortográficos. Así, varios textos en el mismo idioma podían tener formas escritas muy diferentes entre sí.</p>

