	<div class="texto"><h1>¿Qué es la ortografía?</h1> <br>

<p align=justify> La ortografía es el conjunto de reglas y normas que rigen la escritura convencional de una lengua. O, como solemos decirlo popularmente, al modo “correcto” de escribirla. Es el conjunto de normas que regulan la escritura. Esta misma establece un conjunto de reglas para el uso correcto de las letras y los signos de puntuación</p>
<br>
<p align=justify>Este nombre proviene de las voces griegas orthos (“recto” o “estricto”) y graphein (“escritura”), pero comenzó a usarse en español a partir del siglo XVIII, cuando en 1727 se establecieron las primeras normas ortográficas del idioma por parte de la Real Academia Española, al poco rato de su fundación.</p>
<br>
<p align=justify>Hasta ese momento, el español se escribía de maneras muy diversas, a veces atendiendo a la fonía de las palabras, otras a sus orígenes etimológicos y a la tradición latina.</p>
<br>
<p align=justify>De hecho, muchas lenguas varían sus normas ortográficas para adaptarlas a necesidades de distinto orden: a veces como una manera de actualizar la grafía de la lengua, y otras con el propósito de hacer la grafía más simple, más competitiva a nivel internacional y más accesible para los extranjeros.</p>
<br>
<p align=justify>En todo caso, la ortografía consiste en normas convencionales, o sea, acordadas por convención. Por ejemplo, en el caso del español, las 22 academias oficiales que existen en el mundo colaboran para conservar un conjunto estable y universal de normas ortográficas. Hoy, sin embargo, la ortografía española ha variado muy poco desde la publicación en 1854 de la Ortografía de la lengua española.</p>

