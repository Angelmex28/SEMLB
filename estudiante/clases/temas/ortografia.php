    <div class="texto"><h1>Finalidad de la ortografía:</h1>
 <br>

<p align=justify>Las reglas ortográficas, en general, no tienen una relación directa con la comprensión del texto en cuestión. Por ejemplo: si una persona que domina la lengua castellana lee una oración que afirma “Crese la expectatiba de bida en todo el mundo”, no tendrá problemas para entender el enunciado. Su escritura correcta, sin embargo, es “Crece la expectativa de vida en todo el mundo” y de seguro transmite el mensaje de forma más limpia y directa, dado que evita al lector el proceso de corrección.</p>
<br>
<p align=justify>La ortografía, en definitiva, ayuda a la estandarización de una lengua, algo que resulta muy importante cuando existen diversos dialectos en un mismo territorio. Cabe mencionar que las reglas de la ortografía se enseñan durante los primeros años de educación primaria.</p>

