<div class="contenido">
	<div class="titulo"><h1>Propiedades de la Suma</h1></div>
	<div class="texto"><h1>Propiedades:</h1> <br>
		

<p align=justify>1.	Propiedad conmutativa: si se altera el orden de los sumandos no cambia el resultado, de esta forma, a+b=b+a.</p>
<br>
<p align=justify>2.	Propiedad asociativa: un ejemplo de esta propiedad es: a+(b+c) = (a+b)+c </p>
<br>
<p align=justify>3.	Elemento neutro: 0. Para cualquier número a, a + 0 = 0 + a = a. Esto significa que cualquier número sumado con 0
da el mismo número como resultado.</p>
<br>
<p align=justify>4.	Elemento opuesto: Para cualquier número entero, racional, real o complejo a, existe un número −a tal que a + (−a) = (−a) + a = 0. Este número −a se denomina elemento opuesto, y es único para cada a. No existe en algunos conjuntos, como el de los números naturales. </p>
<br>
<p align=justify>5.	Propiedad distributiva: La suma de dos números multiplicada por un tércer número es igual a la suma de cada sumando multiplicado por el tercer número. Por ejemplo 4 * (6+3) = 4*6 + 4*3. </p>
<br>


