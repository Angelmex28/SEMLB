	<div class="titulo"><h1>La Suma</h1></div>
	<div class="texto"><h1>¿Qué es la Suma?</h1> <br>
		<p align=justify>La suma o adición es la operación matemática de combinar o añadir dos números para obtener una cantidad final o total. La suma también ilustra el proceso de juntar dos colecciones de objetos con el fin de obtener una sola colección. Por otro lado, la acción repetitiva de sumar uno, es la forma más básica de contar.</p>
		<br>
		<p align=justify> En términos más formales, la suma es una operación aritmética definida sobre conjuntos de números (naturales, enteros, racionales, reales y complejos) y también sobre estructuras asociadas a ellos, como espacios vectoriales con vectores cuyas componentes sean estos números o funciones que tengan su imagen en ellos.</p>
		<br>
		<p align=justify> En el álgebra moderna se utiliza el nombre suma y su símbolo “+” para representar la operación formal de un anillo que dota al anillo de estructura de grupo abeliano, o la operación de un módulo que dota al módulo de estructura de grupo abeliano. También se utiliza a veces en teoría de grupos para representar la operación que dota a un conjunto de estructura de grupo. En estos casos se trata de una denominación puramente simbólica, sin que necesariamente coincida esta operación con la suma habitual en números, funciones, vectores.</p>
