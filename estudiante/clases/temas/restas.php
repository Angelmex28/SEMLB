	<div class="texto"><h1>¿Qué es la Resta?</h1> <br>
<p align=justify> Es la operación de restar (separar una parte del todo, sacar el residuo de algo, disminuir, rebajar o cercenar). Se trata de una de las cuatro operaciones básicas de las matemáticas y la más sencilla junto a la suma. </p>

<br>
<p align=justify> La resta es una operación de descomposición: dada una cierta cantidad, se elimina una parte de ella y se obtiene un resultado (denominado diferencia). Por ejemplo: si tengo ocho manzanas y regaló cuatro, me quedaré con cuatro manzanas (8-4=4). En otras palabras, a la cantidad ocho le quito cuatro y la diferencia será cuatro. El primer número se conoce como minuendo y el segundo, como sustraendo(minuendo-sustraendo=diferencia). </p>

<br>
<p align=justify> La resta es la operación inversa a la suma: a+b=c, c-b=a (4+2=6, 6-2=4). Es importante tener en cuenta que, en el conjunto de los números naturales, sólo es posible restar dos números si el minuendo (el primer número) es mayor que el sustraendo (el segundo número). Si esto no se cumple, la diferencia (el resultado) será un número negativo (no natural): 5-4=1, 4-5= -1. </p>

<br>
<p align=justify>La posibilidad de restar dos números naturales y obtener un número negativo hace que la resta sea una operación de mayor complejidad que la suma, donde una operación con dos números positivos nunca dará como resultado uno negativo.
La resta en la matemática avanzada, por lo tanto, no consiste en sustraer, sino en sumar el opuesto: no se utiliza la fórmula a-b, sino a+(-b). En este caso, -b es el elemento opuesto de b respecto de la suma.</p>
