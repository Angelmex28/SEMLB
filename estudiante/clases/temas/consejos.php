<div class="contenido">
    <div class="titulo"><h1>Consejos:</h1></div>
    <div class="texto"><h1>2. Recurre a las lecturas con cronómetro</h1>
 <br>

<p align=justify>Una estrategia para ayudar a los niños a leer más rápido consiste en pedirles que lean un texto en un tiempo determinado cometiendo el menor número de errores posible. Observa dónde los niños se detienen o rompen su fluidez y pídeles que repitan la lectura hasta que logren leer el texto sin equivocarse. Si hubo palabras que no pudieron pronunciar, pídeles que las repitan varias veces y motívalos a buscar su significado. Cuando hayan superado sus errores, pídeles que vuelvan a leer el texto con el cronómetro encendido hasta que logren leerlo de forma fluida en un tiempo determinado.</p>


<br>

