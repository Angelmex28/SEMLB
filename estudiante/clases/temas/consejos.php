    <div class="texto"><h1>5. Léeles cada noche antes de dormir</h1>
 <br>

<p align=justify>Leerles a los niños antes de dormir no solo les calma y ayuda a conciliar mejor el sueño, sino que también es una buena estrategia para transmitirles nuevos conocimientos y desarrollar sus habilidades lectoras. Cada lectura le sirve a los niños como modelo para aprender a entonar, hacer inflexiones en la voz y ganar en fluidez y precisión. De hecho, hay muchos niños que llegan a memorizar los textos que sus padres les leen por la noche y son capaces de recitarlos con extraordinaria fluidez.</p>


