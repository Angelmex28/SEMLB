<div class="contenido">
    <div class="titulo"><h1>Consejos:</h1></div>
    <div class="texto"><h1>1. Practica con la lectura modelo:</h1>
 <br>

<p align=justify>Los niños aprenden en gran medida por imitación, sobre todo cuando son pequeños. Por eso, una buena estrategia para mejorar la fluidez de su lectura consiste en leerles textos a modo de ejemplo. Lo ideal es elegir textos que tengan muchos signos de puntuación y diálogos, de manera que tengas que hacer varias pausas e inflexiones de la voz. Luego, puedes pedirles que imiten tu lectura para que entren en estas habilidades. </p>


<br>

