<div class="contenido">
    <div class="titulo"><h1>Consejos:</h1></div>
    <div class="texto"><h1>3. Organiza sesiones de lectura en voz alta</h1>
 <br>

<p align=justify>Las sesiones de lectura son una estrategia excelente para entrenar la habilidad lectora en los niños y mejorar su fluidez. En estos casos, puedes pedirles que lean un texto en voz alta. Aunque para hacerlo más divertido, puedes organizar una sesión de lectura en la que todos lean un pequeño fragmento del texto o en la que todos lean a la vez, un método que se conoce como lectura coral. De esta manera, los niños se verán “obligados” a seguir el ritmo de lectura del resto, lo cual les hará esforzarse para leer de forma más rápida y fluida.</p>


