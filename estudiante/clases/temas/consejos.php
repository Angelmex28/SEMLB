	<div class="titulo"><h1>La Lectura</h1></div>
	<div class="texto"><h1>Consejos de Lectura</h1> <br>

<p align=justify>Aprender a leer es una de las habilidades que más favorecen el desarrollo lingüístico e intelectual de los niños. Estimula la expresión oral, mejora el vocabulario y fomenta el aprendizaje de diversos contenidos desde una edad temprana. De hecho, un estudio realizado por psicólogos de la Universidad de Toronto, en Canadá, desveló que los niños a los que les gusta leer suelen ser más flexibles y creativos, a la vez que son más tolerantes.</p>
<br>
<p align=justify>Asimismo, otra investigación, esta vez publicada en la revista PLoS ONE, encontró que leer estimula la empatía y favorece las relaciones sociales. El estudio, realizado por investigadores de los Países Bajos, demostró que las personas a las que les gusta leer, sobre todo cuando se trata de obras de ficción, son más empáticas, una habilidad que les permite contar con un círculo social más amplio y sólido.</p>
<br>
<p align=justify>Sin embargo, una vez que los niños saben leer y empiezan a aprovechar todos estos beneficios, es importante que ganen en fluidez y velocidad en la lectura. De esta manera, podrán comprender mejor los textos, involucrarse emocionalmente con las historias y asimilar más contenido en menos tiempo.</p>



