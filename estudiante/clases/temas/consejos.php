<div class="contenido">
    <div class="titulo"><h1>Consejos:</h1></div>
    <div class="texto"><h1>4. Anímalos a leer sus libros favoritos</h1>
 <br>

<p align=justify>Al contrario de lo que muchos padres piensan, leer siempre textos nuevos no ayuda a desarrollar la fluidez y la rapidez de la lectura sino todo lo contrario. De hecho, cuanto más acostumbrados están los niños a leer un mismo libro, más fluida será su lectura. A la larga, esto les reportará más confianza en sí mismos, así como una mayor precisión y velocidad a la hora de leer. También les ayuda a comprender mejor el contenido, a la vez que contribuye a potenciar la expresión verbal.</p>


<br>

