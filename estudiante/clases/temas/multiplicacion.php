<div class="contenido">
    <div class="titulo"><h1>Propiedades de la Multiplicación</h1></div>
    <div class="texto"><h1>Propiedades:</h1> <br>


<p align=justify>1.Propiedad conmutativa:
El orden de los factores no varía el producto.</p>
 <br>
<p align=justify>El resultado de multiplicar 10 x 3 será igual que al multiplicar 3 x 10. Aunque cambiemos el orden de los factores el resultado seguirá siendo 30.</p>
<br>
<p align=justify>2.Propiedad asociativa
El modo de agrupar los factores no varía el resultado de la multiplicación.</p>
 <br>
<p align=justify>3.Elemento neutro
El 1 es el elemento neutro de la multiplicación porque todo número multiplicado por él da el mismo número.</p>
<br>
<p align=justify>Por lo tanto cualquier número que multipliquemos por 1, nos dará como resultado el mismo número.</p>
<br>
<p align=justify>4.Propiedad distributiva
La multiplicación de un número por una suma es igual a la suma de las multiplicaciones de dicho número por cada uno de los sumandos.
Pongamos un ejemplo: 2 x (3 + 5).</p>
 <br>
<p align=justify>Según la propiedad distributiva 2 x (3 + 5) será igual a 2 x 3 + 2 x 5
Comprobemos si esto es cierto.
2 x (3 + 5) = 2 x 8 = 16
2 x 3 + 2 x 5 = 6 + 10 = 16
Ambas nos dan como resultado 16, por lo que queda demostrada la propiedad distributiva de la multiplicación.</p>



