	<div class="texto"><h1>¿Qué es la Multiplicación?</h1> <br>

<p align=justify> La multiplicación es una operación matemática que consiste en sumar un número tantas veces como lo indique el otro número que compone la operación, por ejemplo: Multiplicación, La operación anterior indica sumar el primer número tres veces, entonces: Multiplicación, Multiplicación viene del latín multiplicativo, que significa “acción y efecto de multiplicarse”.</p>

<br>
<p align=justify> Del mismo modo, multiplicación fuera del ámbito matemático implica aumentar una misma cosa, bien sea de forma cualitativa o cuantitativa.</p>

<br>
<p align=justify> La multiplicación es una operación binaria que se establece en un conjunto numérico.2 En aritmética, es una de las cuatro operaciones elementales, junto con la suma, la resta y la división, y es la operación inversa de esta última. Esto significa que para toda multiplicación hay una división, por ejemplo 5 por 2 igual a 10 existe una división equivalente, en este caso: 10 dividido entre 2 igual a 5, o 10 dividido entre 5 igual a 2. </p>


