	<div class="titulo"><h1>Las Fracciones</h1></div>
	<div class="texto"><h1>¿Qué es una fracción?</h1> <br>

<p align=justify>Una fracción o número fraccionario es en matemáticas la forma de expresar una cantidad dividida. Una fracción representa el número de partes que cogemos de una unidad que está dividida en partes iguales. Se representa por dos números separados por una línea de fracción.</p>
<br>
<p align=justify>Los términos de una fracción son el numerador y el denominador. El numerador es el número de partes que tenemos y el denominador es el número de partes en que hemos dividido la unidad.</p>
<br>
<p align=justify>Vamos a ver un ejemplo: Tenemos diferentes figuras y cada una de ellas la dividimos en diferentes partes iguales, que es el denominador. La parte coloreada es el numerador.</p>
<br>
<p align=justify>¿Cómo se leen las fracciones? Se leen en función de cuál es su denominador:</p>
<br>
<p>1 / 2: un medio</p>
<br>
<p>1 / 3: un tercio</p>
<br>
<p>1 / 4: un cuarto</p>
<br>
<p>1 / 5: un quinto</p>
<br>
<p>1 / 6: un sexto</p>
<br>
<p>1 / 7: un séptimo</p>
<br>
<p>1 / 8: un octavo</p>
<br>
<p>1 / 9: un noveno</p>
<br>
<p>1 / 10: un décimo</p>
<br>
<p>1 / 11: un onceavo</p>
<br>
<p>1 / 12: un doceavo</p>
<br>
<p>1 / 13: un treceavo</p>

